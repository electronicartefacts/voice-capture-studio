function prompt(input: {
  readonly id: string;
  readonly text: string;
  readonly spokenText?: string;
  readonly primary: string;
  readonly secondary: readonly string[];
  readonly label: string;
  readonly useCase: string;
  readonly emotionLabels: readonly string[];
  readonly valence: number;
  readonly arousal: number;
  readonly dominance: number;
  readonly tone: string;
  readonly pace: PromptDefinition["delivery"]["pace"];
  readonly energy: PromptDefinition["delivery"]["energy"];
  readonly delivery?: Partial<PromptDefinition["delivery"]>;
  readonly context: string;
  readonly directorNote: string;
  readonly pauseInstruction: string;
  readonly emphasis: readonly string[];
  readonly avoid: readonly string[];
  readonly prosody?: Partial<PromptDefinition["prosody"]>;
  readonly phonetics: PromptDefinition["phonetics"];
  readonly qa?: Partial<PromptDefinition["qa"]>;
  readonly tags: readonly string[];
}): PromptDefinition {
  const wordCount = input.text.trim().split(/\s+/).length;

  return {
    id: input.id as PromptId,
    text: input.text,
    spokenText: input.spokenText ?? input.text,
    intention: {
      id: `intention.${input.primary}` as IntentionId,
      primary: input.primary,
      secondary: input.secondary,
      useCase: input.useCase,
      label: input.label,
      emotion: {
        valence: input.valence,
        arousal: input.arousal,
        dominance: input.dominance,
        labels: input.emotionLabels,
      },
    },
    delivery: {
      tone: input.tone,
      pace: input.pace,
      energy: input.energy,
      articulation: input.delivery?.articulation ?? "clear_natural",
      projection: input.delivery?.projection ?? "intimate",
      smile: input.delivery?.smile ?? inferSmile(input.secondary),
      breathiness: input.delivery?.breathiness ?? "low",
      pauseStyle:
        input.delivery?.pauseStyle ??
        inferPauseStyle(input.pace, input.pauseInstruction),
    },
    direction: {
      context: input.context,
      directorNote: input.directorNote,
      pauseInstruction: input.pauseInstruction,
      emphasis: input.emphasis,
      avoid: input.avoid,
    },
    prosody: {
      targetPace: input.prosody?.targetPace ?? input.pace,
      targetPitch: input.prosody?.targetPitch ?? inferPitch(input.energy),
      pitchVariation:
        input.prosody?.pitchVariation ?? inferPitchVariation(input.primary),
      phraseAttack:
        input.prosody?.phraseAttack ?? inferPhraseAttack(input.primary),
      sentenceEnding: input.prosody?.sentenceEnding ?? "falling",
      intimacy: input.prosody?.intimacy ?? "close",
    },
    phonetics: input.phonetics,
    qa: {
      minDurationMs: input.qa?.minDurationMs ?? Math.max(1500, wordCount * 230),
      maxDurationMs: input.qa?.maxDurationMs ?? Math.max(6500, wordCount * 740),
      rejectIf: input.qa?.rejectIf ?? defaultRejectIf,
    },
    tags: input.tags,
  };
}

function inferSmile(
  secondary: readonly string[],
): PromptDefinition["delivery"]["smile"] {
  if (secondary.includes("audible_smile")) {
    return "audible";
  }

  if (
    secondary.includes("slight_smile") ||
    secondary.includes("professional_smile") ||
    secondary.includes("light_smile") ||
    secondary.includes("smile")
  ) {
    return "slight";
  }

  return "none";
}

function inferPauseStyle(
  pace: PromptDefinition["delivery"]["pace"],
  pauseInstruction: string,
): PromptDefinition["delivery"]["pauseStyle"] {
  if (pace === "fast" || pace === "medium_fast") {
    return "urgent";
  }

  if (pauseInstruction.toLowerCase().includes("pause")) {
    return "thoughtful";
  }

  return "structured";
}

function inferPitch(
  energy: PromptDefinition["delivery"]["energy"],
): PromptDefinition["prosody"]["targetPitch"] {
  if (energy === "medium_high" || energy === "high") {
    return "slightly_lifted";
  }

  if (energy === "low") {
    return "lower_stable";
  }

  return "natural";
}

function inferPitchVariation(
  primary: string,
): PromptDefinition["prosody"]["pitchVariation"] {
  if (
    primary.includes("thinking") ||
    primary.includes("question") ||
    primary.includes("surprise") ||
    primary.includes("narrative")
  ) {
    return "medium";
  }

  return "low";
}

function inferPhraseAttack(
  primary: string,
): PromptDefinition["prosody"]["phraseAttack"] {
  if (
    primary.includes("direct") ||
    primary.includes("urgent") ||
    primary.includes("firm") ||
    primary.includes("critique")
  ) {
    return "assertive";
  }

  if (
    primary.includes("warm") ||
    primary.includes("thinking") ||
    primary.includes("narrative") ||
    primary.includes("encouragement")
  ) {
    return "soft";
  }

  return "clean";
}
